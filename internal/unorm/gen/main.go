// Table generator for internal/unorm: downloads the Unicode Character
// Database files for the pinned version, computes fully-recursive
// decompositions, the canonical composition pairs, combining classes, and
// per-form quick-check exception ranges, and writes tables.go. Run
// manually when bumping the Unicode version:
//
//	go run ./internal/unorm/gen
//
// The output is committed; builds and tests never touch the network.
package main

import (
	"fmt"
	"go/format"
	"io"
	"net/http"
	"os"
	"sort"
	"strconv"
	"strings"
)

const ucdVersion = "16.0.0"

func fetch(name string) string {
	url := "https://www.unicode.org/Public/" + ucdVersion + "/ucd/" + name
	resp, err := http.Get(url)
	if err != nil {
		panic(err)
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		panic(name + ": " + resp.Status)
	}
	b, err := io.ReadAll(resp.Body)
	if err != nil {
		panic(err)
	}
	return string(b)
}

func parseHex(s string) rune {
	v, err := strconv.ParseUint(strings.TrimSpace(s), 16, 32)
	if err != nil {
		panic(err)
	}
	return rune(v)
}

type decomp struct {
	runes  []rune
	compat bool
}

func main() {
	ud := fetch("UnicodeData.txt")
	exclusions := fetch("CompositionExclusions.txt")
	dnp := fetch("DerivedNormalizationProps.txt")

	ccc := map[rune]uint8{}
	dec := map[rune]decomp{}
	for _, line := range strings.Split(ud, "\n") {
		f := strings.Split(line, ";")
		if len(f) < 6 {
			continue
		}
		cp := parseHex(f[0])
		if c, err := strconv.Atoi(f[3]); err == nil && c != 0 {
			ccc[cp] = uint8(c)
		}
		if f[5] == "" {
			continue
		}
		d := decomp{}
		parts := strings.Fields(f[5])
		if strings.HasPrefix(parts[0], "<") {
			d.compat = true
			parts = parts[1:]
		}
		for _, p := range parts {
			d.runes = append(d.runes, parseHex(p))
		}
		dec[cp] = d
	}

	excluded := map[rune]bool{}
	for _, line := range strings.Split(exclusions, "\n") {
		line = strings.TrimSpace(line)
		if i := strings.IndexByte(line, '#'); i >= 0 {
			line = strings.TrimSpace(line[:i])
		}
		if line == "" {
			continue
		}
		excluded[parseHex(line)] = true
	}

	// Fully recursive decompositions. Canonical uses canonical mappings
	// only; compatibility applies both kinds until fixpoint (UAX#15).
	// Hangul stays algorithmic and is never in the tables.
	var full func(r rune, compat bool) []rune
	full = func(r rune, compat bool) []rune {
		d, ok := dec[r]
		if !ok || (d.compat && !compat) {
			return []rune{r}
		}
		var out []rune
		for _, x := range d.runes {
			out = append(out, full(x, compat)...)
		}
		return out
	}

	// Primary composites: canonical, non-excluded, two-rune, starter-first
	// decompositions (UAX#15 primary composite definition).
	type pair struct{ a, b, c rune }
	var pairs []pair
	for cp, d := range dec {
		if d.compat || len(d.runes) != 2 || excluded[cp] || ccc[cp] != 0 || ccc[d.runes[0]] != 0 {
			continue
		}
		pairs = append(pairs, pair{d.runes[0], d.runes[1], cp})
	}
	sort.Slice(pairs, func(i, j int) bool {
		if pairs[i].a != pairs[j].a {
			return pairs[i].a < pairs[j].a
		}
		return pairs[i].b < pairs[j].b
	})

	// Quick-check exceptions per form: runes whose QC property is No or
	// Maybe (DerivedNormalizationProps), plus every nonzero-ccc rune --
	// a string of QC-Yes starters in code-point order is normalized in
	// that form with no further work.
	qc := map[string]map[rune]bool{"NFC": {}, "NFD": {}, "NFKC": {}, "NFKD": {}}
	for _, line := range strings.Split(dnp, "\n") {
		if i := strings.IndexByte(line, '#'); i >= 0 {
			line = line[:i]
		}
		f := strings.Split(line, ";")
		if len(f) < 3 {
			continue
		}
		prop := strings.TrimSpace(f[1])
		var form string
		switch prop {
		case "NFC_QC":
			form = "NFC"
		case "NFD_QC":
			form = "NFD"
		case "NFKC_QC":
			form = "NFKC"
		case "NFKD_QC":
			form = "NFKD"
		default:
			continue
		}
		rng := strings.TrimSpace(f[0])
		lo, hi := rng, rng
		if i := strings.Index(rng, ".."); i >= 0 {
			lo, hi = rng[:i], rng[i+2:]
		}
		for r := parseHex(lo); r <= parseHex(hi); r++ {
			qc[form][r] = true
		}
	}

	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by internal/unorm/gen from UCD %s; DO NOT EDIT.\n", ucdVersion)
	fmt.Fprintf(&b, "// Generated tables are exempt from the repo file-size norm.\n\npackage unorm\n\n")
	fmt.Fprintf(&b, "// UnicodeVersion is the UCD the tables were generated from.\nconst UnicodeVersion = %q\n\n", ucdVersion)

	writeRunes := func(name string, rs []rune) {
		fmt.Fprintf(&b, "var %s = [...]rune{", name)
		for i, r := range rs {
			if i%12 == 0 {
				b.WriteString("\n\t")
			}
			fmt.Fprintf(&b, "%#x, ", r)
		}
		b.WriteString("\n}\n\n")
	}

	// cccTab: sorted (rune, class) pairs for nonzero combining classes.
	var cccRunes []rune
	for r := range ccc {
		cccRunes = append(cccRunes, r)
	}
	sort.Slice(cccRunes, func(i, j int) bool { return cccRunes[i] < cccRunes[j] })
	writeRunes("cccKey", cccRunes)
	fmt.Fprintf(&b, "var cccVal = [...]uint8{")
	for i, r := range cccRunes {
		if i%16 == 0 {
			b.WriteString("\n\t")
		}
		fmt.Fprintf(&b, "%d, ", ccc[r])
	}
	b.WriteString("\n}\n\n")

	// Decomposition tables: sorted keys; values index a shared rune pool
	// as (offset << 8 | length) -- decompositions are short.
	writeDecomp := func(prefix string, compat bool) {
		var keys []rune
		for r := range dec {
			if !compat && dec[r].compat {
				continue
			}
			keys = append(keys, r)
		}
		sort.Slice(keys, func(i, j int) bool { return keys[i] < keys[j] })
		var pool []rune
		var idx []uint32
		for _, r := range keys {
			f := full(r, compat)
			if len(f) > 255 {
				panic("decomposition too long")
			}
			idx = append(idx, uint32(len(pool))<<8|uint32(len(f)))
			pool = append(pool, f...)
		}
		writeRunes(prefix+"Key", keys)
		fmt.Fprintf(&b, "var %sIdx = [...]uint32{", prefix)
		for i, v := range idx {
			if i%12 == 0 {
				b.WriteString("\n\t")
			}
			fmt.Fprintf(&b, "%#x, ", v)
		}
		b.WriteString("\n}\n\n")
		writeRunes(prefix+"Pool", pool)
	}
	writeDecomp("canon", false)
	writeDecomp("compat", true)

	// Composition pairs: sorted by (first, second) for binary search.
	var pa, pb, pc []rune
	for _, p := range pairs {
		pa, pb, pc = append(pa, p.a), append(pb, p.b), append(pc, p.c)
	}
	writeRunes("compFirst", pa)
	writeRunes("compSecond", pb)
	writeRunes("compResult", pc)

	// Quick-check exception ranges per form (QC No/Maybe), merged with
	// nonzero-ccc runes so one sorted-range scan answers "definitely
	// normalized starters only".
	writeRanges := func(name string, m map[rune]bool) {
		var rs []rune
		for r := range m {
			rs = append(rs, r)
		}
		for _, r := range cccRunes {
			if !m[r] {
				rs = append(rs, r)
			}
		}
		sort.Slice(rs, func(i, j int) bool { return rs[i] < rs[j] })
		var lo, hi []rune
		for i := 0; i < len(rs); {
			j := i
			for j+1 < len(rs) && rs[j+1] == rs[j]+1 {
				j++
			}
			lo = append(lo, rs[i])
			hi = append(hi, rs[j])
			i = j + 1
		}
		writeRunes(name+"Lo", lo)
		writeRunes(name+"Hi", hi)
	}
	writeRanges("qcNFC", qc["NFC"])
	writeRanges("qcNFD", qc["NFD"])
	writeRanges("qcNFKC", qc["NFKC"])
	writeRanges("qcNFKD", qc["NFKD"])

	src, err := format.Source([]byte(b.String()))
	if err != nil {
		panic(err)
	}
	if err := os.WriteFile("internal/unorm/tables.go", src, 0o644); err != nil {
		panic(err)
	}
	fmt.Printf("tables.go: %d ccc, %d canon, %d compat, %d comp pairs\n",
		len(cccRunes), 0, 0, len(pairs))
}
